subroutine design_quad ( xc, r, func_3d, order, quad )

!*****************************************************************************80
!
!! DESIGN_QUAD approximates an integral with a design of a given order.
!
!  Discussion:
!
!    The design is defined for the unit sphere in 3D, and provides a
!    guaranteed accuracy for integrals of polynomials of a given
!    degree over the surface of the unit sphere.
!
!    The same guarantee applies if the sphere is scaled to have
!    a non-unit radius R.
!
!    The same guarantee applies if the center of the sphere is
!    translated by XC.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    08 June 2002
!
!  Author:
!
!    John Burkardt
!
!  Input:
!
!    real ( kind = 8 ) XC(3), the coordinates of the center of the sphere.
!
!    real ( kind = 8 ) R, the radius of the sphere.
!
!    real ( kind = 8 ) external FUNC_3D, evaluates the function.
!
!    integer ( kind = 4 ) ORDER, the desired order of the design.
!
!  Output:
!
!    real ( kind = 8 ) QUAD, the approximate integral of the function.
!
  implicit none

  real ( kind = 8 ), external :: func_3d
  integer ( kind = 4 ) i
  integer ( kind = 4 ) j
  integer ( kind = 4 ) n
  integer ( kind = 4 ) order
  real ( kind = 8 ) quad
  real ( kind = 8 ) r
  real ( kind = 8 ), allocatable, dimension ( :, : ) :: x
  real ( kind = 8 ) xc(3)

  call design_size ( order, n )

  if ( n == 0 ) then
    quad = 0.0D+00
    return
  end if

  allocate ( x(1:3,1:n) )

  call design_points ( order, n, x )

  x(1:3,1:n) = r * x(1:3,1:n)

  do i = 1, 3
    x(i,1:n) = x(i,1:n) + xc(i)
  end do

  if ( .false. ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'The design points:'
    write ( *, '(a)' ) ' '
    do j = 1, n
      write ( *, '(i6,3g14.6)' ) j, x(1:3,j)
    end do
  end if

  quad = 0.0D+00
  do j = 1, n
    quad = quad + func_3d ( x(1:3,j) )
  end do

  quad = quad * r**2 / real ( n, kind = 8 )

  deallocate ( x )

  return
end
