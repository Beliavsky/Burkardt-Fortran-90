subroutine twb_rule_n ( strength, n )

!*****************************************************************************80
!
!! twb_rule_n returns the order of a TWB rule of given strength.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    16 April 2019
!
!  Author:
!
!    John Burkardt
!
!  Reference:
!
!    Mark Taylor, Beth Wingate, Len Bos, 
!    Several new quadrature formulas for polynomial integration in the triangle, 
!    http://arxiv.org/abs/math/0501496v2,
!    08 February 2007.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) STRENGTH, the desired strength.
!    1 <= STRENGTH.
!
!    Output, integer ( kind = 4 ) N, the order of the rule, if it exists.
!    Otherwise, the value -1 is returned.
!
  implicit none

  integer ( kind = 4 ) n
  integer ( kind = 4 ) strength

  if ( strength == 1 ) then
    n = 1
  else if ( strength == 2 ) then
    n = 3
  else if ( strength == 4 ) then
    n = 6
  else if ( strength == 5 ) then
    n = 10
  else if ( strength == 7 ) then
    n = 15
  else if ( strength == 9 ) then
    n = 21
  else if ( strength == 11 ) then
    n = 28
  else if ( strength == 13 ) then
    n = 36
  else if ( strength == 14 ) then
    n = 45
  else if ( strength == 16 ) then
    n = 55
  else if ( strength == 18 ) then
    n = 66
  else if ( strength == 20 ) then
    n = 78
  else if ( strength == 21 ) then
    n = 91
  else if ( strength == 23 ) then
    n = 105
  else if ( strength == 25 ) then
    n = 120
  else
    n = -1
  end if

  return
end
