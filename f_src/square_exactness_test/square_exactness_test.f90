subroutine legendre_2d_set ( a, b, nx, ny, x, y, w )

!*****************************************************************************80
!
!! LEGENDRE_2D_SET: set a 2D Gauss-Legendre quadrature rule.
!
!  Discussion:
!
!    The integral:
!
!      integral ( a(2) <= y <= b(2) ) ( a(1) <= x <= b(1) ) f(x,y) dx dy
!
!    The quadrature rule:
!
!      sum ( 1 <= i <= n ) w(i) * f ( x(i),y(i) )
!
!    where n = nx * ny, the orders of the rule in the X and Y directions.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    29 May 2014
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, real ( kind = 8 ) A(2), B(2), the lower and upper integration
!    limits.
!
!    Input, integer ( kind = 4 ) NX, NY, the orders in the X and Y directions.
!    NX and NY must be between 1 and 10.
!
!    Output, real ( kind = 8 ) X(N), Y(N), the abscissas.
!
!    Output, real ( kind = 8 ) W(N), the weights.
!
  implicit none

  integer ( kind = 4 ) nx
  integer ( kind = 4 ) ny

  real ( kind = 8 ) a(2)
  real ( kind = 8 ) b(2)
  integer ( kind = 4 ) i
  integer ( kind = 4 ) j
  integer ( kind = 4 ) k
  real ( kind = 8 ) w(nx*ny)
  real ( kind = 8 ) wx(nx)
  real ( kind = 8 ) wy(ny)
  real ( kind = 8 ) x(nx*ny)
  real ( kind = 8 ) xx(nx)
  real ( kind = 8 ) y(nx*ny)
  real ( kind = 8 ) yy(ny)
!
!  Get the rules for [-1,+1].
!
  call legendre_set ( nx, xx, wx )
  call legendre_set ( ny, yy, wy )
!
!  Adjust from [-1,+1] to [A,B].
!
  do i = 1, nx
    xx(i) = ( ( 1.0D+00 - xx(i) ) * a(1)   &
            + ( 1.0D+00 + xx(i) ) * b(1) ) &
            /   2.0D+00
    wx(i) = wx(i) * ( b(1) - a(1) ) / 2.0D+00
  end do

  do j = 1, ny
    yy(j) = ( ( 1.0D+00 - yy(j) ) * a(2)   &
            + ( 1.0D+00 + yy(j) ) * b(2) ) &
            /   2.0D+00
    wy(j) = wy(j) * ( b(2) - a(2) ) / 2.0D+00
  end do
!
!  Compute the product rule.
!
  k = 0
  do j = 1, ny
    do i = 1, nx
      k = k + 1
      x(k) = xx(i)
      y(k) = yy(j)
      w(k) = wx(i) * wy(j)
    end do
  end do

  return
end
