  real ( kind = 8 ) b(2)
  integer ( kind = 4 ) l
  integer ( kind = 4 ) n
  integer ( kind = 4 ) n_1d
  integer ( kind = 4 ) p_max
  integer ( kind = 4 ) t
  real ( kind = 8 ), allocatable :: w(:)
  real ( kind = 8 ), allocatable :: x(:)
  real ( kind = 8 ), allocatable :: y(:)

  a(1:2) = -1.0D+00
  b(1:2) = +1.0D+00

  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) 'TEST01'
  write ( *, '(a)' ) '  Product Gauss-Legendre rules for the 2D Legendre integral.'
  write ( *, '(a)' ) '  Density function rho(x) = 1.'
  write ( *, '(a)' ) '  Region: -1 <= x <= +1.'
  write ( *, '(a)' ) '  Region: -1 <= y <= +1.'
  write ( *, '(a)' ) '  Level: L'
  write ( *, '(a)' ) '  Exactness: 2*L+1'
  write ( *, '(a)' ) '  Order: N = (L+1)*(L+1)'

  do l = 0, 5

    n_1d = l + 1
    n = n_1d * n_1d
    t = 2 * l + 1

    allocate ( x(1:n) )
    allocate ( y(1:n) )
    allocate ( w(1:n) )

