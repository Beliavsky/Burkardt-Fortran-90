subroutine padua_weights ( l, w )

!*****************************************************************************80
!
!! PADUA_WEIGHTS returns quadrature weights do Padua points.
!
!  Discussion:
!
!    The order of the weights corresponds to the ordering used
!    by the companion function padua_points().
!
!    Caliari, de Marchi and Vianello supplied a MATLAB code pdwtsMM
!    which carries out this same computation in a way that makes
!    more efficient use of MATLAB's vector and matrix capabilities.  
!    This version of the computation was painfully rewritten to display 
!    the individual scalar computations, so that it could be translated
!    into other languages.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    10 June 2014
!
!  Author:
!
!    John Burkardt
!
!  Reference:
!
!    Marco Caliari, Stefano de Marchi, Marco Vianello,
!    Bivariate interpolation on the square at new nodal sets,
!    Applied Mathematics and Computation,
!    Volume 165, Number 2, 2005, pages 261-274.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) L, the level of the set.
!    0 <= L
!
!    Output, real ( kind = 8 ) W((L+1)*(L+2)/2), the quadrature weights.
!
  implicit none

  integer ( kind = 4 ) l

  real ( kind = 8 ) angle
  integer ( kind = 4 ) i
  integer ( kind = 4 ) i2
  integer ( kind = 4 ) j
  integer ( kind = 4 ) j2
  integer ( kind = 4 ) lp1h
  integer ( kind = 4 ) lp2h
  integer ( kind = 4 ) lp3h
  real ( kind = 8 ) mi
  real ( kind = 8 ) mj
  real ( kind = 8 ), allocatable :: mom(:,:)
  real ( kind = 8 ), parameter :: r8_pi = 3.141592653589793D+00
  real ( kind = 8 ), allocatable :: te1(:,:)
  real ( kind = 8 ), allocatable :: te2(:,:)
  real ( kind = 8 ), allocatable :: tmteo(:,:)
  real ( kind = 8 ), allocatable :: tmtoe(:,:)
  real ( kind = 8 ), allocatable :: to1(:,:)
  real ( kind = 8 ), allocatable :: to2(:,:)
  real ( kind = 8 ) w(((l+1)*(l+2))/2)
  real ( kind = 8 ), allocatable :: w1(:,:)
  real ( kind = 8 ), allocatable :: w2(:,:)

  if ( l == 0 ) then
    w(1) = 4.0D+00
    return
  end if
!
!  Relatives of L/2:
!
  lp1h = ( l + 1 ) / 2
  lp2h = ( l + 2 ) / 2
  lp3h = ( l + 3 ) / 2
!
!  TE1, TE2, TO1, TO2: 
!  Even and odd Chebyshev polynomials on subgrids 1 and 2.
!
  allocate ( te1(1:lp2h,1:lp2h) )
  do j = 1, lp2h
    do i = 1, lp2h
      angle = r8_pi * real ( 2 * ( i - 1 ) * ( 2 * j - 2 ), kind = 8 ) &
        / real ( l, kind = 8 )
      te1(i,j) = cos ( angle )
    end do
  end do
  te1(2:lp2h,1:lp2h) = te1(2:lp2h,1:lp2h) * sqrt ( 2.0D+00 )

  allocate ( to1(1:lp2h,1:lp1h) )
  do j = 1, lp1h
    do i = 1, lp2h
      angle = r8_pi * real ( 2 * ( i - 1 ) * ( 2 * j - 1 ), kind = 8 ) &
        / real ( l, kind = 8 )
      to1(i,j) = cos ( angle )
    end do
  end do
  to1(2:lp2h,1:lp1h) = to1(2:lp2h,1:lp1h) * sqrt ( 2.0D+00 )

  allocate ( te2(1:lp2h,1:lp3h) )
  do j = 1, lp3h
    do i = 1, lp2h
      angle = r8_pi * real ( 2 * ( i - 1 ) * ( 2 * j - 2 ), kind = 8 ) &
        / real ( l + 1, kind = 8 )
      te2(i,j) = cos ( angle )
    end do
  end do
  te2(2:lp2h,1:lp3h) = te2(2:lp2h,1:lp3h) * sqrt ( 2.0D+00 )

  allocate ( to2(1:lp2h,1:lp2h) )
  do j = 1, lp2h
    do i = 1, lp2h
      angle = r8_pi * real ( 2 * ( i - 1 ) * ( 2 * j - 1 ), kind = 8 ) &
        / real ( l + 1, kind = 8 )
      to2(i,j) = cos ( angle )
    end do
  end do
  to2(2:lp2h,1:lp2h) = to2(2:lp2h,1:lp2h) * sqrt ( 2.0D+00 )
!
!  MOM: Moments matrix do even * even pairs.
!
  allocate ( mom(1:lp2h,1:lp2h) )

  do j = 1, lp2h
    mj = 2.0D+00 * sqrt ( 2.0D+00 ) / real ( 1 - ( 2 * j - 2 ) ** 2, kind = 8 )
    do i = 1, lp2h + 1 - j
      mi = 2.0D+00 * sqrt ( 2.0D+00 ) &
        / real ( 1 - ( 2 * i - 2 ) ** 2, kind = 8 )
      mom(i,j) = mi * mj
    end do
  end do

  mom(1,1:lp2h) = mom(1,1:lp2h) / sqrt ( 2.0D+00 )
  mom(1:lp2h,1) = mom(1:lp2h,1) / sqrt ( 2.0D+00 )

  if ( mod ( l, 2 ) == 0 ) then
    mom(lp2h,1) = mom(lp2h,1) / 2.0D+00
  end if
!
!  TMTOE and TMTEO: matrix products.
!
  allocate ( tmtoe(1:lp2h,1:lp2h) )
  tmtoe(1:lp2h,1:lp2h) = 0.0D+00

  do j2 = 1, lp2h
    do i2 = 1, lp2h + 1 - j2
      do j = 1, lp2h
        do i = 1, lp2h
          tmtoe(i,j) = tmtoe(i,j) + to2(i2,i) * mom(j2,i2) * te1(j2,j)
        end do
      end do
    end do
  end do

  allocate ( tmteo(1:lp3h,1:lp1h) )
  tmteo(1:lp3h,1:lp1h) = 0.0D+00

  do j2 = 1, lp2h
    do i2 = 1, lp2h + 1 - j2
      do j = 1, lp1h
        do i = 1, lp3h
          tmteo(i,j) = tmteo(i,j) + te2(i2,i) * mom(j2,i2) * to1(j2,j)
        end do
      end do
    end do
  end do
!
!  W1 and W2: Interpolation weight matrices.
!
  allocate ( w1(1:lp2h,1:lp2h) )
  w1(1:lp2h,1:lp2h) = 2.0D+00 / real ( l * ( l + 1 ), kind = 8 )

  w1(1:lp2h,1) = w1(1:lp2h,1) / 2.0D+00

  if ( mod ( l, 2 ) == 0 ) then
    w1(1:lp2h,lp2h) = w1(1:lp2h,lp2h) / 2.0D+00
    w1(lp2h,1:lp2h) = w1(lp2h,1:lp2h) / 2.0D+00
  end if

  allocate ( w2(1:lp3h,1:lp1h) )
  w2(1:lp3h,1:lp1h) = 2.0D+00  / real ( l * ( l + 1 ), kind = 8 )

  w2(1,1:lp1h) = w2(1,1:lp1h) / 2.0D+00

  if ( mod ( l, 2 ) == 1 ) then
    w2(lp3h,1:lp1h) = w2(lp3h,1:lp1h) / 2.0D+00
    w2(1:lp3h,lp1h) = w2(1:lp3h,lp1h) / 2.0D+00
  end if
!
!  Cubature weights as matrices on the subgrids.
!
  do j = 1, lp2h
    do i = 1, lp2h
      w1(i,j) = w1(i,j) * tmtoe(i,j)
    end do
  end do

  do j = 1, lp1h
    do i = 1, lp3h
      w2(i,j) = w2(i,j) * tmteo(i,j)
    end do
  end do
!
!  Pack weight matrices W1 and W2 into the vector W.
!
  if ( mod ( l, 2 ) == 0 ) then

    do j = 1, lp2h
      do i = 1, lp2h
        w(i+(2*j-2)*lp2h) = w1(i,j)
      end do
    end do

    do j = 1, lp1h
      do i = 1, lp3h
        w(i+(2*j-1)*lp2h) = w2(i,j)
      end do
    end do

  else

    do j = 1, lp1h
      do i = 1, lp2h
        w(i+(j-1)*(l+2)) = w1(i,j)
      end do
    end do

    do j = 1, lp1h
      do i = 1, lp3h
        w(i+lp2h+(j-1)*(l+2)) = w2(i,j)
      end do
    end do

  end if
!
!  Free memory.
!
  deallocate ( te1 )
  deallocate ( te2 )
  deallocate ( tmteo )
  deallocate ( tmtoe )
  deallocate ( to1 )
  deallocate ( to2 )
  deallocate ( w1 )
  deallocate ( w2 )

  return
end
subroutine r8mat_transpose_print ( m, n, a, title )

!*****************************************************************************80
!
!! R8MAT_TRANSPOSE_PRINT prints an R8MAT, transposed.
!
!  Discussion:
!
!    An R8MAT is an MxN array of R8's, stored by (I,J) -> [I+J*M].
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    14 June 2004
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) M, N, the number of rows and columns.
