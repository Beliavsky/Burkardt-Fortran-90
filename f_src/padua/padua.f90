subroutine filename_inc ( filename )

!*****************************************************************************80
!
!! FILENAME_INC increments a partially numeric filename.
!
!  Discussion:
!
!    It is assumed that the digits in the name, whether scattered or
!    connected, represent a number that is to be increased by 1 on
!    each call.  If this number is all 9's on input, the output number
!    is all 0's.  Non-numeric letters of the name are unaffected.
!
!    If the name is empty, then the routine stops.
!
!    If the name contains no digits, the empty string is returned.
!
!  Example:
!
!      Input            Output
!      -----            ------
!      'a7to11.txt'     'a7to12.txt'
!      'a7to99.txt'     'a8to00.txt'
!      'a9to99.txt'     'a0to00.txt'
!      'cat.txt'        ' '
!      ' '              STOP!
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    19 September 2012
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input/output, character ( len = * ) FILENAME.
!    On input, a character string to be incremented.
!    On output, the incremented string.
!
  implicit none

  character c
  integer ( kind = 4 ) change
  integer ( kind = 4 ) digit
  character ( len = * ) filename
  integer ( kind = 4 ) i
  integer ( kind = 4 ) lens

  lens = len_trim ( filename )

  if ( lens <= 0 ) then
    write ( *, '(a)' ) ' '
    write ( *, '(a)' ) 'FILENAME_INC - Fatal error!'
    write ( *, '(a)' ) '  The input string is empty.'
    stop 1
  end if

  change = 0

  do i = lens, 1, -1

    c = filename(i:i)

    if ( lge ( c, '0' ) .and. lle ( c, '9' ) ) then

      change = change + 1

      digit = ichar ( c ) - 48
      digit = digit + 1

      if ( digit == 10 ) then
        digit = 0
      end if

      c = char ( digit + 48 )

      filename(i:i) = c

      if ( c /= '0' ) then
        return
      end if

    end if

  end do
!
!  No digits were found.  Return blank.
!
  if ( change == 0 ) then
    filename = ' '
    return
  end if

  return
end
subroutine get_unit ( iunit )

!*****************************************************************************80
!
!! GET_UNIT returns a free FORTRAN unit number.
!
!  Discussion:
!
!    A "free" FORTRAN unit number is a value between 1 and 99 which
!    is not currently associated with an I/O device.  A free FORTRAN unit
!    number is needed in order to open a file with the OPEN command.
!
!    If IUNIT = 0, then no free FORTRAN unit could be found, although
!    all 99 units were checked (except for units 5, 6 and 9, which
!    are commonly reserved for console I/O).
!
!    Otherwise, IUNIT is a value between 1 and 99, representing a
!    free FORTRAN unit.  Note that GET_UNIT assumes that units 5 and 6
!    are special, and will never return those values.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license. 
!
!  Modified:
!
!    26 October 2008
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Output, integer ( kind = 4 ) IUNIT, the free unit number.
!
  implicit none

  integer ( kind = 4 ) i
  integer ( kind = 4 ) ios
  integer ( kind = 4 ) iunit
  logical ( kind = 4 ) lopen
 
  iunit = 0
 
  do i = 1, 99
 
    if ( i /= 5 .and. i /= 6 .and. i /= 9 ) then

      inquire ( unit = i, opened = lopen, iostat = ios )
 
      if ( ios == 0 ) then
        if ( .not. lopen ) then
          iunit = i
          return
        end if
      end if

    end if
 
  end do

  return
end
subroutine padua_order ( l, n )

!*****************************************************************************80
!
!! PADUA_ORDER returns the size of the Padua set of given level.
!
!  Discussion:
!
!    The Padua sets are indexed by a level that starts at 0.
!    This function returns the number of points in each level.
!
!  Example:
!
!    Level   Size
!    -----   ----
!        0      1
!        1      3
!        2      6
!        3     10
!        4     15
!        5     21
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    09 February 2014
!
!  Author:
!
!    John Burkardt
!
!  Reference:
!
!    Marco Caliari, Stefano de Marchi, Marco Vianello,
!    Bivariate interpolation on the square at new nodal sets,
!    Applied Mathematics and Computation,
!    Volume 165, Number 2, 2005, pages 261-274.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) L, the level of the set.
!    0 <= L
!
!    Output, integer ( kind = 4 ) N, the order (number of points) in the set.
!
  implicit none

  integer ( kind = 4 ) i
  integer ( kind = 4 ) l
  integer ( kind = 4 ) n

  n = 0
  do i = 0, l
    n = n + ( l / 2 ) + 1
    if ( mod ( l, 2 ) == 1 .and. mod ( i, 2 ) == 1 ) then
      n = n + 1
    end if
  end do

  return
end
subroutine padua_plot ( l, filename )

!*****************************************************************************80
!
!! PADUA_PLOT plots the Padua points of given level.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    29 May 2014
!
!  Author:
!
!    John Burkardt
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) L, the level of the set.
!    0 <= L
!
!    Input, character ( len = * ) FILENAME, a common filename prefix for 
!    the files to be created.
!
  implicit none

  character ( len = 255 ) command_filename
  integer ( kind = 4 ) command_unit
  character ( len = 255 ) data_filename
  integer ( kind = 4 ) data_unit
  character ( len = * ) filename
  integer ( kind = 4 ) j
  integer ( kind = 4 ) l
  integer ( kind = 4 ) n
  character ( len = 255 ) plot_filename
  real ( kind = 8 ), allocatable :: xy(:,:)

  n = ( ( l + 1 ) * ( l + 2 ) ) / 2

  allocate ( xy(1:2,1:n) )

  call padua_points ( l, xy )
!
!  Create graphics data file.
!
  call get_unit ( data_unit )
  data_filename = trim ( filename ) // '_data.txt'
  open ( unit = data_unit, file = data_filename, status = 'replace' )
  do j = 1, n
    write ( data_unit, '(2x,g24.16,2x,g24.16)' ) xy(1:2,j)
  end do
  close ( unit = data_unit )
  write ( *, '(a)' ) ' '
  write ( *, '(a)' ) '  Created data file "' // trim ( data_filename ) // '".'
!
!  Create graphics command file.
!
  call get_unit ( command_unit )
  command_filename = trim ( filename ) // '_commands.txt'
  open ( unit = command_unit, file = command_filename, status = 'replace' )
  write ( command_unit, '(a)' ) '# ' // trim ( command_filename )
  write ( command_unit, '(a)' ) '#'
  write ( command_unit, '(a)' ) '# Usage:'
  write ( command_unit, '(a)' ) '#  gnuplot < ' // trim ( command_filename )
  write ( command_unit, '(a)' ) '#'
  write ( command_unit, '(a)' ) 'set term png'
  plot_filename = trim ( filename ) // '.png'
  write ( command_unit, '(a)' ) 'set output "' // trim ( plot_filename ) // '"'
  write ( command_unit, '(a)' ) 'set xlabel "<--- X --->"'
  write ( command_unit, '(a)' ) 'set ylabel "<--- Y --->"'
  write ( command_unit, '(a,i2,a)' ) 'set title "Padua Points, Level ', l, '"'
  write ( command_unit, '(a)' ) 'set grid'
  write ( command_unit, '(a)' ) 'set key off'
  write ( command_unit, '(a)' ) 'set size ratio -1'
  write ( command_unit, '(a)' ) 'set style data lines'
  write ( command_unit, '(a)' ) 'set timestamp'
  write ( command_unit, '(a)' ) 'plot [-1:+1] [-1:+1] "' // &
    trim ( data_filename ) // &
    '" using 1:2 with points lt 3 pt 3'
  close ( unit = command_unit )

  write ( *, '(a)' ) &
    '  Created command file "' // trim ( command_filename ) // '".'
!
!  Free memory.
!
  deallocate ( xy )

  return
end
subroutine padua_points ( l, xy )

!*****************************************************************************80
!
!! PADUA_POINTS returns the Padua points of order N.
!
!  Licensing:
!
!    This code is distributed under the GNU LGPL license.
!
!  Modified:
!
!    09 June 2014
!
!  Author:
!
!    John Burkardt
!
!  Reference:
!
!    Marco Caliari, Stefano de Marchi, Marco Vianello,
!    Bivariate interpolation on the square at new nodal sets,
!    Applied Mathematics and Computation,
!    Volume 165, Number 2, 2005, pages 261-274.
!
!  Parameters:
!
!    Input, integer ( kind = 4 ) L, the level of the set.
!    0 <= L
!
!    Output, real ( kind = 8 ) XY(2,((L+1)*(L+2))/2), the Padua points.
!
  implicit none

  integer ( kind = 4 ) l

  real ( kind = 8 ) angle1
  real ( kind = 8 ) angle2
  integer ( kind = 4 ) i
  integer ( kind = 4 ) j
  integer ( kind = 4 ) j_hi
  integer ( kind = 4 ) k
  real ( kind = 8 ), parameter :: r8_pi = 3.141592653589793D+00
  real ( kind = 8 ) xy(2,((l+1)*(l+2))/2)

  if ( l == 0 ) then
    xy(1,1) = 0.0D+00
    xy(2,1) = 0.0D+00
    return
  end if

  k = 0

  do i = 0, l

    j_hi = ( l / 2 ) + 1
    if ( mod ( l, 2 ) == 1 .and. mod ( i, 2 ) == 1 ) then
      j_hi = j_hi + 1
    end if

    do j = 1, j_hi

     k = k + 1

      if ( i * 2 == l ) then
        xy(1,k) = 0.0D+00
      else
        angle1 = real ( i, kind = 8 ) * r8_pi / real ( l, kind = 8 )
        xy(1,k) = cos ( angle1 )
      end if

      if ( mod ( i, 2 ) == 0 ) then
        if ( 2 * ( 2 * j - 1 ) == l + 1 ) then
          xy(2,k) = 0.0D+00
        else
          angle2 = real ( 2 * j - 1, kind = 8 ) * r8_pi &
                 / real ( l + 1, kind = 8 )
          xy(2,k) = cos ( angle2 )
        end if
      else
        if ( 2 * ( 2 * j - 2 ) == l + 1 ) then
          xy(2,k) = 0.0D+00
        else
          angle2 = real ( 2 * j - 2, kind = 8 ) * r8_pi &
                 / real ( l + 1, kind = 8 )
          xy(2,k) = cos ( angle2 )
        end if
      end if

    end do

  end do

  return
end
